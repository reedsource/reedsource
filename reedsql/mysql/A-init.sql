DROP TABLE IF EXISTS 员工表;
DROP TABLE IF EXISTS 部门表;
DROP TABLE IF EXISTS 工资级别表;

CREATE TABLE 部门表
(
    部门编号 int(2) not null,
    部门名称 VARCHAR(14),
    部门位置 VARCHAR(13),
    primary key (部门编号)
);
CREATE TABLE 员工表
(
    员工编号 int(4) not null,
    员工姓名 VARCHAR(10),
    工作   VARCHAR(9),
    领导编号 INT(4),
    受雇日期 DATE DEFAULT NULL,
    月薪   DOUBLE(7, 2),
    津贴   DOUBLE(7, 2),
    部门编号 INT(2),
    primary key (员工编号)
)
;

CREATE TABLE 工资级别表
(
    等级   INT,
    最低工资 INT,
    最高工资 INT
);



